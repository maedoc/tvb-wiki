"""
Ralph Crosslink Applier Agent — actually applies crosslink suggestions
and scans for inline link opportunities.

Runs daily. Replaces the old weekly crosslink.py (which only generated suggestions).

Three layers:
  Layer 0 (mechanical): Scan body text for plain-text mentions of existing
                        page titles/slugs and wrap them in [[wikilinks]].
  Layer 1 (suggestions): Load crosslink_suggestions.json (generated by
                          crosslink.py) and apply high-confidence suggestions.
  Layer 2 (LLM):        For high-value suggestions with no mechanical insertion
                         point, use cheap model to insert naturally.
"""
